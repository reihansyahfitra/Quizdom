    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.jTextField1.getText().isBlank()||this.jTextField2.getText().isBlank()||this.jTextField3.getText().isBlank()||this.jTextField4.getText().isBlank()){
            this.jLabel1.setText("CANNOT BE EMPTY!");
        }else{
            String id = this.jTextField1.getText();
            String fullname = this.jTextField2.getText();
            String username = this.jTextField3.getText();
            String password = this.jTextField4.getText();
            if(this.jTextField1.getText().charAt(0)=='1'){
                this.u = new Admin(id, fullname, username);
                this.sub.insert(u, password);
            }else if(this.jTextField1.getText().charAt(0)=='2'){
                this.u = new Dosen(id, fullname, username);
                this.sub.insert(u, password);
            }else if(this.jTextField1.getText().charAt(0)=='3' && !this.jTextField5.getText().isBlank()){
                String lecturerNip = this.jTextField5.getText();
                this.u = new Mahasiswa(id, lecturerNip, fullname, username);
                this.sub.insert(u, password);
            }else{
                this.jLabel1.setText("INVALID INPUT!");
            }
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

